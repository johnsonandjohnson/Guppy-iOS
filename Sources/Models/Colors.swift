import UIKit

struct Colors {
    
    struct Cell {
        
        static let backgroundBlue = #colorLiteral(red: 0.003921568627, green: 0.3725490196, blue: 0.6431372549, alpha: 1)
    }

    struct Status {
        
        // Use a darker green than the systemGreen against
        // white/light backgrounds so it's easier to read
        static var darkGreen: UIColor {
            return UIColor { traitCollection -> UIColor in
                if traitCollection.userInterfaceStyle == .dark {
                    return .systemGreen
                } else {
                    return #colorLiteral(red: 0, green: 0.3921568627, blue: 0, alpha: 1)
                }
            }
        }
    }
    
    struct NavigationBar {
        
        static let lightBlue = #colorLiteral(red: 0.01176470588, green: 0.5647058824, blue: 0.8549019608, alpha: 1)
    }
}
